def normalize_ui_language(code: str | None) -> str:
    raw = (code or "").strip()
    if raw.lower().startswith("zh"):
        return CHINESE_UI_LANGUAGE
    return DEFAULT_UI_LANGUAGE


def set_language(code: str | None) -> str:
    global _current_language
    _current_language = normalize_ui_language(code)
    return _current_language


def current_language() -> str:
    return _current_language


def translate(key: str, *, locale: str | None = None, **kwargs: Any) -> str:
    language = normalize_ui_language(locale) if locale is not None else _current_language
    template = _TRANSLATIONS.get(language, {}).get(key) or _TRANSLATIONS[DEFAULT_UI_LANGUAGE].get(key) or key
    return template.format(**kwargs)


def ui_language_options(*, locale: str | None = None) -> tuple[tuple[str, str], ...]:
    language = normalize_ui_language(locale) if locale is not None else _current_language
    return (
        (DEFAULT_UI_LANGUAGE, _TRANSLATIONS[language]["language.name.en"]),
        (CHINESE_UI_LANGUAGE, _TRANSLATIONS[language]["language.name.zh-CN"]),
    )


def status_text(status: str, *, locale: str | None = None) -> str:
    key = _STATUS_KEYS.get(status)
    return translate(key, locale=locale) if key else status


def todo_reason_text(reason: str, *, locale: str | None = None) -> str:
    key = _TODO_REASON_KEYS.get(reason)
    return translate(key, locale=locale) if key else reason


def history_kind_text(kind: str, *, locale: str | None = None) -> str:
    key = _HISTORY_KIND_KEYS.get(kind)
    return translate(key, locale=locale) if key else kind
